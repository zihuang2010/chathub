// 7 天伪随机趋势：用一个种子让同一账号每次刷新数据稳定，但数字看起来"有起伏"。
function seedTrend(seed: number, base: number): number[] {
  const out: number[] = [];
  let s = seed;
  for (let i = 0; i < 7; i++) {
    s = (s * 9301 + 49297) % 233_280;
    const v = Math.floor(base * 0.4 + (s / 233_280) * base);
    out.push(v);
  }
  return out;
}

