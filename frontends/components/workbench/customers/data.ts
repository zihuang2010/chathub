// 跨账号铺开，让多张卡片都有非零数字。
const daysAgoStamp = (d: number, hour = 9) =>
  new Date(Date.now() - d * 86_400_000 + hour * 3_600_000)
    .toISOString()
    .replace("T", " ")
    .slice(0, 16);

