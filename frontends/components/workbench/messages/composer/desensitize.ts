// AI 润色脱敏:把发往外部 LLM 的「对话背景」里的敏感信息遮成占位,降低客户隐私外泄。
//
// 仅用于对话背景转录(只读语境),不处理待润色草稿——草稿是客服正要发给客户的话,需保留
// 真实值,故原样发送(见 AiPolishPopover 的 originalText 路径)。
//
// 用正则近似识别,非穷尽:覆盖手机号 / 身份证 / 银行卡 / 邮箱。地址、姓名等难以用正则可靠
// 识别,暂不纳入。用 `\b` 数字边界(而非后行断言 lookbehind)以兼容老旧 WebView——含 lookbehind
// 的正则字面量在不支持的引擎上会在模块加载期抛 SyntaxError,反而拖垮整个输入框。

// 顺序很重要:先匹配长/具体的模式,再匹配短/泛化的,避免银行卡号被手机号规则切碎。
// 邮箱 → 身份证(18 位) → 银行卡(16-19 位) → 手机号(11 位)。
const RULES: { re: RegExp; mask: string }[] = [
  // 邮箱。
  { re: /[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}/g, mask: "[邮箱]" },
  // 身份证(18 位:省份码 + 出生年月日 + 顺序码 + 校验位 0-9/X)。日期结构降低误匹配。
  {
    re: /\b[1-9]\d{5}(?:18|19|20)\d{2}(?:0[1-9]|1[0-2])(?:0[1-9]|[12]\d|3[01])\d{3}[\dXx]\b/g,
    mask: "[身份证]",
  },
  // 银行卡(16-19 位连续数字)。`\b` 防止把更长数字串切出一段误判。
  { re: /\b\d{16,19}\b/g, mask: "[银行卡]" },
  // 手机号(11 位,1 开头、第二位 3-9)。
  { re: /\b1[3-9]\d{9}\b/g, mask: "[手机号]" },
];

/** 对一段文本做脱敏替换。无敏感信息时原样返回。 */
export function desensitize(text: string): string {
  let out = text;
  for (const { re, mask } of RULES) out = out.replace(re, mask);
  return out;
}
